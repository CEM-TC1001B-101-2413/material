print(type(15)) # int -> integer -> número entero
print(type(12.1)) # float -> número real
print(type(15j)) # complex -> número complejo
print(type("Hola")) # str -> string -> texto
print(type(True)) # bool -> booleano -> Verdadero/Falso
print(type(False))
# Conjunto de datos
print(type([12, True, "Hola", [1,2]])) # list
# lista -> Conjunto ordenado y mutable de datos
print(type((12, True, "Hola", (1,2)))) # tuple
# tupla -> Conjunto ordenado e inmutable de datos
print(type({"hola": 12, "manzana": [1,2,3]})) # dict
# diccionario -> Conjunto ordenado, mutable y nombrado de datos